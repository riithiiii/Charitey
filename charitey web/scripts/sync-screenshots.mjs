import fs from "fs";
import path from "path";

const root = process.cwd();
const sourceDir = path.join(root, "Assets", "App Screenshots");
const targetDir = path.join(root, "public", "images", "screenshots");
const outputFile = path.join(root, "lib", "generated", "screenshots.ts");

function syncScreenshots() {
  if (!fs.existsSync(sourceDir)) {
    console.warn("Assets/App Screenshots not found — keeping existing screenshots.");
    return;
  }

  fs.mkdirSync(targetDir, { recursive: true });
  fs.mkdirSync(path.dirname(outputFile), { recursive: true });

  const files = fs
    .readdirSync(sourceDir)
    .filter((file) => /\.(jpe?g|png|webp)$/i.test(file))
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }));

  if (files.length === 0) {
    console.warn("No screenshots found in Assets/App Screenshots.");
    return;
  }

  const existing = fs.existsSync(targetDir)
    ? fs.readdirSync(targetDir).filter((file) => !file.startsWith("."))
  : [];

  for (const file of existing) {
    if (file.startsWith("screenshot-")) {
      fs.unlinkSync(path.join(targetDir, file));
    }
  }

  const paths = [];

  files.forEach((file, index) => {
    const ext = path.extname(file).toLowerCase();
    const normalized = `screenshot-${index + 1}${ext === ".jpg" ? ".jpeg" : ext}`;
    fs.copyFileSync(path.join(sourceDir, file), path.join(targetDir, normalized));
    paths.push(`/images/screenshots/${normalized}`);
  });

  const content = `/** Auto-generated by scripts/sync-screenshots.mjs — do not edit manually */\nexport const screenshotPaths = ${JSON.stringify(paths, null, 2)} as const;\n`;

  fs.writeFileSync(outputFile, content, "utf-8");
  console.log(`Synced ${paths.length} screenshots.`);
}

syncScreenshots();
